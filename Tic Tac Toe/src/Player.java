public class Player {

    private int score;
    private String name;

    //getters
    public int getScore() {
        return score;
    }
    public String getName() {
        return name;
    }

    // setters
    public void setScore(int newScore) {
        score = newScore;
    } 
    public void setName(String newName) {
        name = newName;
    }
}
